import { NextResponse } from 'next/server';
import { createClient } from '@supabase/supabase-js';

// Supabase 서버용 클라이언트 초기화 (Service Role Key 사용 필수)
const supabase = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL!,
  process.env.SUPABASE_SERVICE_ROLE_KEY!
);

export async function GET() {
  try {
    // 1. 공공데이터포털 API 호출 (pSize를 300으로 설정하여 전체 데이터 확보)
    const response = await fetch(
      `https://open.assembly.go.kr/portal/openapi/nwvrqwxyaytdsfvhu?KEY=${process.env.NATIONAL_ASSEMBLY_API_KEY}&Type=json&pSize=300`
    );
    
    if (!response.ok) {
      throw new Error('API 호출 실패');
    }

    const data = await response.json();
    const members = data.nwvrqwxyaytdsfvhu[1].row;

    // 2. Supabase에 데이터 Upsert (이름 기준 중복 방지)
    for (const m of members) {
      await supabase.from('politicians').upsert({
        name: m.HG_NM,               // 이름
        hj_name: m.HJ_NM || '',      // 한자 이름
        birth_date: m.BTH_DATE || '',// 생년월일
        district: m.ORIG_NM || '',   // 지역구
        party: m.POLY_NM || '무소속', // 정당
        committee: m.CMIT_NM || '미정', // 상임위
        election_count: m.RELE_GUBUN || '초선', // 선수
        homepage: m.HOMEPAGE || '',  // 홈페이지
        office_phone: m.MONA_CD || '', // 의원실 전화
        secretary: m.SECRETARY || '',  // 보좌관
        secretary2: m.SECRETARY2 || '',// 비서관
        asist: m.ASIST || ''           // 비서
      }, { onConflict: 'name' });
    }

    return NextResponse.json({ 
      success: true, 
      message: `${members.length}명의 데이터가 성공적으로 동기화되었습니다.` 
    });

  } catch (error) {
    console.error('동기화 오류:', error);
    return NextResponse.json({ 
      success: false, 
      error: '데이터 동기화 중 오류가 발생했습니다.' 
    }, { status: 500 });
  }
}
