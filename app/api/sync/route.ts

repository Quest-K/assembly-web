import { NextResponse } from 'next/server';

// 이 코드는 API 경로가 올바르게 작동하는지 확인하는 테스트용입니다.
export async function GET() {
  try {
    return NextResponse.json({ 
      status: "success",
      message: "API 경로가 정상적으로 설정되었습니다.",
      timestamp: new Date().toISOString()
    });
  } catch (error) {
    return NextResponse.json({ 
      status: "error",
      message: "서버 응답 오류" 
    }, { status: 500 });
  }
}
