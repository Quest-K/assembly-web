import { NextResponse } from 'next/server';
import { createClient } from '@supabase/supabase-js';

const supabase = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL!,
  process.env.SUPABASE_SERVICE_ROLE_KEY!
);

export async function GET() {
  try {
    const response = await fetch(`https://open.assembly.go.kr/portal/openapi/nwvrqwxyaytdsfvhu?KEY=${process.env.NATIONAL_ASSEMBLY_API_KEY}&Type=json&pSize=300`);
    const data = await response.json();
    const members = data.nwvrqwxyaytdsfvhu[1].row;

    for (const m of members) {
      await supabase.from('politicians').upsert({
        name: m.HG_NM,
        hj_name: m.HJ_NM,
        birth_date: m.BTH_DATE,
        district: m.ORIG_NM,
        party: m.POLY_NM,
        committee: m.CMIT_NM,
        election_count: m.RELE_GUBUN,
        homepage: m.HOMEPAGE,
        office_phone: m.MONA_CD,
        secretary: m.SECRETARY,
        secretary2: m.SECRETARY2,
        asist: m.ASIST
      }, { onConflict: 'name' });
    }

    return NextResponse.json({ success: true, message: `${members.length}명의 데이터가 동기화되었습니다.` });
  } catch (error) {
    return NextResponse.json({ success: false, error: '동기화 실패' }, { status: 500 });
  }
}
