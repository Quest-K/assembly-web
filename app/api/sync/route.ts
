import { NextResponse } from 'next/server';
import { createClient } from '@supabase/supabase-js';

// Vercel에 추가한 환경 변수를 서버에서 가져옵니다.
const supabase = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL!,
  process.env.SUPABASE_SERVICE_ROLE_KEY! // 이제 이게 작동합니다!
);

export async function GET() {
  try {
    const response = await fetch(
      `https://open.assembly.go.kr/portal/openapi/nwvrqwxyaytdsfvhu?KEY=${process.env.NATIONAL_ASSEMBLY_API_KEY}&Type=json&pSize=300`
    );
    
    if (!response.ok) throw new Error('API 호출 실패');
    
    const data = await response.json();
    const members = data.nwvrqwxyaytdsfvhu[1].row;

    // 데이터 저장/업데이트 루프
    for (const m of members) {
      await supabase.from('politicians').upsert({
        name: m.HG_NM,
        hj_name: m.HJ_NM || '',
        birth_date: m.BTH_DATE || '',
        district: m.ORIG_NM || '',
        party: m.POLY_NM || '무소속',
        committee: m.CMIT_NM || '미정',
        election_count: m.RELE_GUBUN || '초선',
        homepage: m.HOMEPAGE || '',
        office_phone: m.MONA_CD || '',
        secretary: m.SECRETARY || '',
        secretary2: m.SECRETARY2 || '',
        asist: m.ASIST || ''
      }, { onConflict: 'name' });
    }

    return NextResponse.json({ success: true, count: members.length });
  } catch (error) {
    console.error('동기화 오류:', error);
    return NextResponse.json({ success: false, error: '서버 저장 실패' }, { status: 500 });
  }
}
